import { Scene } from "./scene.js";

// ############################################################################################
// ############################################################################################
// ############################################################################################

window.addEventListener(
    "load",
    (event) => {
        console.log("starting webgl app");
        app_maim();
    }
);

// ############################################################################################
// ############################################################################################
// ############################################################################################

// ------------------------------------------------
// ------------------------------------------------
// ------- settings

// just needed to be before the main call?
let fps = 40;
var timeBetweenFrames = 1000.0/fps;
//   0.0 to 1.0:     [   R,   G,   B,   A ]
var canvasClearColour = [ 0.1, 0.1, 0.1, 1.0 ];


// ------------------------------------------------
// ------------------------------------------------

var canvas_01;
var gl_context_01;

// ------------------------------------------------
// ------------------------------------------------

var scene;

// ------------------------------------------------
// ------------------------------------------------

var oldTime;

// ------------------------------------------------
// ------------------------------------------------

// ############################################################################################
// ############################################################################################
// ############################################################################################

function getCanvasElement(canvas_name){
    return document.querySelector(`#${canvas_name}`);
}

function gl_context_init(){

    // weird way to grab the canvas element, shouldnt we getElementById?
    canvas_01 = getCanvasElement("webgl_canvas_perlin_01");
    
    // init the GL context
    gl_context_01 = canvas_01.getContext("webgl");
}

// ############################################################################################
// ############################################################################################
// ############################################################################################

function canvas_init(){
    gl_context_01.clearColor(canvasClearColour[0], canvasClearColour[1], canvasClearColour[2], canvasClearColour[3]); // clear to black
    gl_context_01.clearDepth(1.0); // clear everything

    gl_context_01.enable(gl_context_01.DEPTH_TEST); // enable depth testing
    gl_context_01.depthFunc(gl_context_01.LEQUAL); // near things obscure far things
    
    gl_context_01.enable(gl_context_01.CULL_FACE);
    gl_context_01.cullFace(gl_context_01.FRONT);
    
    gl_context_01.enable(gl_context_01.BLEND);
    gl_context_01.blendFunc(gl_context_01.SRC_ALPHA, gl_context_01.ONE_MINUS_SRC_ALPHA);
    // gl_context_01.blendFunc(gl_context_01.ONE, gl_context_01.ONE_MINUS_SRC_ALPHA);

    scene = new Scene( gl_context_01 );

    oldTime = Date.now();
}

// ############################################################################################
// ############################################################################################
// ############################################################################################

function canvas_update(deltaTime){
    // do update
    scene.update(deltaTime);
}

function canvas_draw() {
    // draw the scene
    scene.draw();
}

function frameUpdate( newTime ){
    // ... generate delta time
    const deltaTime = (newTime - oldTime)/1000.0;
    oldTime = newTime;
    // do update
    canvas_update(deltaTime);
    // then draw
    canvas_draw();
}

// ############################################################################################
// ############################################################################################
// ############################################################################################

// entry point
function app_maim() {

    // ======================================================================
    // ======================================================================
    // ======================================================================
    // ======== prepare the canvas stuffs

    gl_context_init();

    // only continue if webGL is available and working
    if( null === gl_context_01 ){
        alert(
            "unable to init webGL. your device may not support it"
        );
        return;
    }

    // ======================================================================
    // ======================================================================
    // ======================================================================
    // ======== initialise things
    
    canvas_init();

    // ======================================================================
    // ======================================================================
    // ======================================================================
    // ======== do drawing



    setInterval(
            function () {
                requestAnimationFrame(
                        (t) => frameUpdate( t )
                    );
            },
            timeBetweenFrames
        );


    // ======================================================================
    // ======================================================================
    // ======================================================================
}



